      <h2>1. Who We Are</h2>
      <p>GuildQuote LLC ("GuildQuote", "we", "us") is a Texas limited liability company that provides estimate generation software for painting contractors. This policy describes how we collect, use, and protect your information.</p>

      <h2>2. Information We Collect</h2>

      <h3>2a. Account Information (from you)</h3>
      <ul>
        <li>Name, email address, company name</li>
        <li>Google account information (when you connect Google OAuth)</li>
        <li>Billing information (processed by Stripe — we do not store card numbers)</li>
      </ul>

      <h3>2b. Business Data (from you)</h3>
      <ul>
        <li>Pricing catalogs and configuration</li>
        <li>Estimate scopes (rooms, surfaces, items, conditions)</li>
        <li>Generated estimates (pricing, production hours, materials)</li>
        <li>Estimate outcomes (accepted, declined, close price)</li>
      </ul>

      <h3>2c. Third-Party Personal Information (entered by you)</h3>
      <ul>
        <li>Your clients' names, email addresses, phone numbers, and property addresses</li>
        <li>We process this data on your behalf to generate and deliver estimates</li>
        <li>You are the data controller for your clients' information; we are the data processor</li>
      </ul>

      <h3>2d. Automatically Collected</h3>
      <ul>
        <li>IP address, browser type, device type</li>
        <li>Pages visited, features used, timestamps</li>
        <li>We use this for service improvement and security — not advertising</li>
      </ul>

      <h2>3. How We Use Your Information</h2>
      <ul>
        <li><strong>Provide the Service:</strong> Generate estimates, create documents, deliver emails, track outcomes</li>
        <li><strong>Improve the Service:</strong> Analyze usage patterns, fix bugs, develop features</li>
        <li><strong>Communicate:</strong> Send account notifications, billing receipts, product updates</li>
        <li><strong>Aggregate analytics:</strong> We may create anonymized, aggregated datasets from usage data (pricing patterns, win rates, market benchmarks) that cannot identify you or your clients. This aggregate data helps us improve default pricing, develop industry benchmarks, and enhance the platform for all users.</li>
      </ul>

      <h2>4. What We Do NOT Do</h2>
      <ul>
        <li>We do <strong>not</strong> sell your personal information to third parties</li>
        <li>We do <strong>not</strong> sell your clients' personal information</li>
        <li>We do <strong>not</strong> use your data for advertising</li>
        <li>We do <strong>not</strong> share your individual business data with competitors</li>
        <li>We do <strong>not</strong> store credit card numbers (Stripe handles all payment processing)</li>
      </ul>

      <h2>5. Data Sharing</h2>
      <p>We share data only with:</p>
      <ul>
        <li><strong>Google:</strong> To create documents in your Drive and send emails via your Gmail (only with your authorization)</li>
        <li><strong>Stripe:</strong> To process payments (see <a href="https://stripe.com/privacy" target="_blank" rel="noopener">Stripe's Privacy Policy</a>)</li>
        <li><strong>Hosting providers:</strong> For infrastructure (data encrypted in transit and at rest)</li>
        <li><strong>Law enforcement:</strong> Only when legally required</li>
      </ul>

      <h2>6. Data Security</h2>
      <p>We protect your data with:</p>
      <ul>
        <li>HTTPS encryption for all data in transit</li>
        <li>Encrypted database storage</li>
        <li>Secure session management</li>
        <li>Access controls limiting employee access to production data</li>
      </ul>
      <p>No system is 100% secure. We will notify you promptly in the event of a data breach affecting your information.</p>

      <h2>7. Your Rights</h2>
      <p>You have the right to:</p>
      <ul>
        <li><strong>Access:</strong> Request a copy of all data we hold about you</li>
        <li><strong>Correct:</strong> Update inaccurate information through your account settings</li>
        <li><strong>Delete:</strong> Request deletion of your account and all associated data</li>
        <li><strong>Export:</strong> Request a machine-readable export of your data</li>
        <li><strong>Object:</strong> Opt out of aggregate data usage by contacting us</li>
      </ul>
      <p>To exercise these rights, email <strong>privacy@guildquote.com</strong>.</p>

      <h2>8. Data Retention</h2>
      <ul>
        <li><strong>Active accounts:</strong> Data retained while your account is active</li>
        <li><strong>Canceled accounts:</strong> Data retained for 90 days, then permanently deleted</li>
        <li><strong>Aggregate data:</strong> Anonymized aggregate data may be retained indefinitely (it cannot identify you)</li>
      </ul>

      <h2>9. California Residents (CCPA)</h2>
      <p>If you are a California resident, you have additional rights under the California Consumer Privacy Act:</p>
      <ul>
        <li>Right to know what personal information we collect and how we use it</li>
        <li>Right to delete personal information</li>
        <li>Right to opt out of the sale of personal information (we do not sell personal information)</li>
        <li>Right to non-discrimination for exercising your rights</li>
      </ul>

      <h2>10. Cookies</h2>
      <p>We use a single session cookie to keep you logged in. We do not use tracking cookies, advertising cookies, or third-party analytics cookies.</p>

      <h2>11. Children</h2>
      <p>GuildQuote is not intended for use by anyone under 18. We do not knowingly collect information from minors.</p>

      <h2>12. Changes to This Policy</h2>
      <p>We may update this policy with 30 days' notice via email. The "Last updated" date at the top reflects the current version.</p>

      <h2>13. Contact</h2>
